struct Sha3Ctx_st {
    uint64_t s[25];
    uint8_t sb[25 * 8];
    uint64_t saved;
    size_t byte_index;
    size_t word_index;
    size_t capacity_words;
    size_t xof_flag;
    Sha3Variant variant;
};

#define ROL64(x, y) ((((x) << ((uint64_t)(y) & 63)) | (((x) & 0xFFFFFFFFFFFFFFFFULL) >> (((uint64_t)64 - ((y) & 63)) & 63))) & 0xFFFFFFFFFFFFFFFFULL)

#define STORE64L(x, y)                                                                     \
  do { (y)[7] = (uint8_t)(((x)>>56)&255); (y)[6] = (uint8_t)(((x)>>48)&255);   \
       (y)[5] = (uint8_t)(((x)>>40)&255); (y)[4] = (uint8_t)(((x)>>32)&255);   \
       (y)[3] = (uint8_t)(((x)>>24)&255); (y)[2] = (uint8_t)(((x)>>16)&255);   \
       (y)[1] = (uint8_t)(((x)>>8)&255); (y)[0] = (uint8_t)((x)&255); } while(0)

#define LOAD64L(x, y)                                                       \
  do { x = (((uint64_t)((y)[7] & 255))<<56)|(((uint64_t)((y)[6] & 255))<<48)| \
           (((uint64_t)((y)[5] & 255))<<40)|(((uint64_t)((y)[4] & 255))<<32)| \
           (((uint64_t)((y)[3] & 255))<<24)|(((uint64_t)((y)[2] & 255))<<16)| \
           (((uint64_t)((y)[1] & 255))<<8)|(((uint64_t)((y)[0] & 255))); } while(0)

static const uint64_t s_keccakf_rndc[24] = {
   0x0000000000000001ULL, 0x0000000000008082ULL,
   0x800000000000808aULL, 0x8000000080008000ULL,
   0x000000000000808bULL, 0x0000000080000001ULL,
   0x8000000080008081ULL, 0x8000000000008009ULL,
   0x000000000000008aULL, 0x0000000000000088ULL,
   0x0000000080008009ULL, 0x000000008000000aULL,
   0x000000008000808bULL, 0x800000000000008bULL,
   0x8000000000008089ULL, 0x8000000000008003ULL,
   0x8000000000008002ULL, 0x8000000000000080ULL,
   0x000000000000800aULL, 0x800000008000000aULL,
   0x8000000080008081ULL, 0x8000000000008080ULL,
   0x0000000080000001ULL, 0x8000000080008008ULL
};

static const size_t s_keccakf_rotc[24] = {
   1, 3, 6, 10, 15, 21, 28, 36, 45, 55, 2, 14, 27, 41, 56, 8, 25, 43, 62, 18, 39, 61, 20, 44
};

static const size_t s_keccakf_piln[24] = {
   10, 7, 11, 17, 18, 3, 5, 16, 8, 21, 24, 4, 15, 23, 19, 13, 12, 2, 20, 14, 22, 9, 6, 1
};

static void s_keccakf(uint64_t s[25])
{
    size_t i, j, round;
    uint64_t t, bc[5];

    for (round = 0; round < 24; round++) {
        /* Theta */
        for (i = 0; i < 5; i++) {
            bc[i] = s[i] ^ s[i + 5] ^ s[i + 10] ^ s[i + 15] ^ s[i + 20];
        }

        for (i = 0; i < 5; i++) {
            t = bc[(i + 4) % 5] ^ ROL64(bc[(i + 1) % 5], 1);
            for (j = 0; j < 25; j += 5) {
                s[j + i] ^= t;
            }
        }

        /* Rho Pi */
        t = s[1];
        for (i = 0; i < 24; i++) {
            j = s_keccakf_piln[i];
            bc[0] = s[j];
            s[j] = ROL64(t, s_keccakf_rotc[i]);
            t = bc[0];
        }

        /* Chi */
        for (j = 0; j < 25; j += 5) {
            for (i = 0; i < 5; i++) {
                bc[i] = s[j + i];
            }

            for (i = 0; i < 5; i++) {
                s[j + i] ^= (~bc[(i + 1) % 5]) & bc[(i + 2) % 5];
            }
        }
        /* Iota */
        s[0] ^= s_keccakf_rndc[round];
    }
}

static void ss_done(Sha3Ctx* ctx, uint8_t* H, uint64_t pad)
{
    size_t i;

    ctx->s[ctx->word_index] ^= (ctx->saved ^ (pad << (ctx->byte_index * 8)));
    ctx->s[25 - ctx->capacity_words - 1] ^= 0x8000000000000000ULL;
    s_keccakf(ctx->s);

    for (i = 0; i < 25; i++) {
        STORE64L(ctx->s[i], ctx->sb + i * 8);
    }

    memcpy(H, ctx->sb, ctx->capacity_words * 4);
}

Sha3Ctx* sha3_alloc(Sha3Variant variant)
{
    int ret = RET_OK;
    Sha3Ctx* ctx = NULL;

    CALLOC_CHECKED(ctx, sizeof(Sha3Ctx));

    switch (variant) {
    case SHA3_VARIANT_224:
        ctx->capacity_words = 2 * 224 / (8 * sizeof(uint64_t));
        break;
    case SHA3_VARIANT_256:
        ctx->capacity_words = 2 * 256 / (8 * sizeof(uint64_t));
        break;
    case SHA3_VARIANT_384:
        ctx->capacity_words = 2 * 384 / (8 * sizeof(uint64_t));
        break;
    case SHA3_VARIANT_512:
        ctx->capacity_words = 2 * 512 / (8 * sizeof(uint64_t));
        break;
    case SHA3_VARIANT_SHAKE128:
        ctx->capacity_words = 2 * 128 / (8 * sizeof(uint64_t));
        break;
    case SHA3_VARIANT_SHAKE256:
        ctx->capacity_words = 2 * 256 / (8 * sizeof(uint64_t));
        break;
    default:
        SET_ERROR(RET_INVALID_PARAM);
    }

    ctx->variant = variant;

cleanup:

    if (ret != RET_OK) {
        sha3_free(ctx);
        ctx = NULL;
    }

    return ctx;
}

int sha3_update(Sha3Ctx* ctx, const ByteArray* msg)
{
    int ret = RET_OK;

    size_t old_tail;
    size_t words;
    size_t tail;
    size_t i;
    const uint8_t* in;
    size_t inlen;

    CHECK_PARAM(ctx != NULL);
    CHECK_PARAM(msg != NULL);

    inlen = msg->len;

    if (inlen == 0) {
        goto cleanup;
    }

    in = msg->buf;
    old_tail = (8 - ctx->byte_index) & 7;

    if (inlen < old_tail) {
        while (inlen--) {
            ctx->saved |= (uint64_t)(*(in++)) << ((ctx->byte_index++) * 8);
        }

        goto cleanup;
    }

    if (old_tail != 0) {
        inlen -= old_tail;
        while (old_tail--) {
            ctx->saved |= (uint64_t)(*(in++)) << ((ctx->byte_index++) * 8);
        }
        ctx->s[ctx->word_index] ^= ctx->saved;
        ctx->byte_index = 0;
        ctx->saved = 0;
        if (++ctx->word_index == (25 - ctx->capacity_words)) {
            s_keccakf(ctx->s);
            ctx->word_index = 0;
        }
    }

    words = inlen / sizeof(uint64_t);
    tail = inlen - words * sizeof(uint64_t);

    for (i = 0; i < words; i++, in += sizeof(uint64_t)) {
        uint64_t t;
        LOAD64L(t, in);
        ctx->s[ctx->word_index] ^= t;
        if (++ctx->word_index == (25 - ctx->capacity_words)) {
            s_keccakf(ctx->s);
            ctx->word_index = 0;
        }
    }

    while (tail--) {
        ctx->saved |= (uint64_t)(*(in++)) << ((ctx->byte_index++) * 8);
    }

cleanup:
    return ret;
}

int sha3_final(Sha3Ctx *ctx, ByteArray **hash_code)
{
    int ret = RET_OK;
    Sha3Variant variant;
    size_t capacity_words;

    CHECK_PARAM(ctx != NULL);
    CHECK_PARAM(hash_code != NULL);

    CHECK_NOT_NULL(*hash_code = ba_alloc_by_len(ctx->capacity_words * 4));
    ss_done(ctx, (*hash_code)->buf, 0x06ULL);

    // ініціалізація для можливості повторного використання
    variant = ctx->variant;
    capacity_words = ctx->capacity_words;

    memset(ctx, 0, sizeof(Sha3Ctx));

    ctx->capacity_words = capacity_words;
    ctx->variant = variant;

cleanup:
    return ret;
}

int sha3_shake_final(Sha3Ctx* ctx, ByteArray* out)
{
    int ret = RET_OK;
    size_t idx, i, outlen;

    CHECK_PARAM(ctx != NULL);
    CHECK_PARAM(out != NULL);

    outlen = out->len;

    if (outlen == 0) {
        return RET_OK;
    }

    if (!ctx->xof_flag) {
        ctx->s[ctx->word_index] ^= (ctx->saved ^ (0x1FULL << (ctx->byte_index * 8)));
        ctx->s[25 - ctx->capacity_words - 1] ^= 0x8000000000000000ULL;
        s_keccakf(ctx->s);

        for (i = 0; i < 25; i++) {
            STORE64L(ctx->s[i], ctx->sb + i * 8);
        }

        ctx->byte_index = 0;
        ctx->xof_flag = 1;
    }

    for (idx = 0; idx < outlen; idx++) {
        if (ctx->byte_index >= (25 - ctx->capacity_words) * 8) {
            s_keccakf(ctx->s);

            for (i = 0; i < 25; i++) {
                STORE64L(ctx->s[i], ctx->sb + i * 8);
            }
            ctx->byte_index = 0;
        }
        out->buf[idx] = ctx->sb[ctx->byte_index++];
    }

cleanup:
    return ret;
}

Sha3Ctx* sha3_copy_with_alloc(const Sha3Ctx* ctx)
{
    Sha3Ctx* out = NULL;
    int ret = RET_OK;

    CALLOC_CHECKED(out, sizeof(Sha3Ctx));
    memcpy(out, ctx, sizeof(Sha3Ctx));

cleanup:

    return out;
}

size_t sha3_get_block_size(const Sha3Ctx* ctx)
{
    if (ctx != NULL) {
        switch (ctx->variant) {
        case SHA3_VARIANT_224:
            return 144;
        case SHA3_VARIANT_256:
            return 136;
        case SHA3_VARIANT_384:
            return 104;
        case SHA3_VARIANT_512:
            return 72;
        default:
            return 0;
        }
    }
    return 0;
}

void sha3_free(Sha3Ctx* ctx)
{
    if (ctx) {
        secure_zero(ctx, sizeof(Sha3Ctx));
        free(ctx);
    }
}

